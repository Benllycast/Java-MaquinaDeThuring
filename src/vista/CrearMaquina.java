    private void crearCintaDeLaMaquina(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearCintaDeLaMaquina
        String cadenaTexto = jTextField1.getText().trim();
        if(cadenaTexto.equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "ingrese la cadena a ser comprovada", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        cadena = new Cadena();


        //simbolo "#" inicial
        cadena.agregarSimbolo(parent.getAdministrador(),"#");
        

        for(int i = 0; i <cadenaTexto.length(); i++){
            System.out.println(cadenaTexto.substring(i, i+1));
            if(parent.getAdministrador().getSimbolo(cadenaTexto.substring(i, i+1)) == null){
                JOptionPane.showMessageDialog(this, "el carcater \""+cadenaTexto.substring(i, i+1)+"\" no se encuentra en la lista de simbolos", "Error", JOptionPane.ERROR_MESSAGE);
                cadena.resetAll(parent.getAdministrador());
                return;
            }else{
                cadena.agregarSimbolo(parent.getAdministrador(), cadenaTexto.substring(i, i+1));
            }
        }


        //simbolo "#" final
        cadena.agregarSimbolo(parent.getAdministrador(),"#");


        parent.setCinta(new Cinta(cadena));
        JOptionPane.showMessageDialog(this, "Todos los caracteres han sido agregados", "Crear Maquina", JOptionPane.INFORMATION_MESSAGE);
        cargarSimbolos();
        jButton1.setEnabled(false);
        jButton3.setEnabled(true);
    }//GEN-LAST:event_crearCintaDeLaMaquina

