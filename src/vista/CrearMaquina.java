    private void load() {

        Iterator i = parent.getConjuntoEstados().getListEstados().iterator();
        while(i.hasNext()){
            jComboBox1.addItem(((Estado)i.next()).getnombre());
        }

        if(parent.getCinta()!= null && parent.getCinta().getCadena()!=null){
               this.cadena = parent.getCinta().getCadena();
               jTextField1.setText(parent.getCinta().getCadenaActual());
               cargarSimbolos();
               jButton1.setEnabled(false);
               jButton3.setEnabled(true);
        }
    }

    private void cargarSimbolos() {
        ListIterator<Simbolo> i = parent.getCinta().getCadena().getListaSimbolos().listIterator();
        borrarTodasLasFilas((DefaultTableModel)jTable_Cadena.getModel());
        while(i.hasNext()){
            Simbolo actual = (Simbolo)i.next();
            ((DefaultTableModel)jTable_Cadena.getModel()).addRow(new Object[]{i.nextIndex(),actual.getsimbolo()});
        }
    }

    public void borrarTodasLasFilas(DefaultTableModel dtm){
        for (int i = dtm.getRowCount() -1; i >= 0; i--){
            dtm.removeRow(i);
        }
    }

